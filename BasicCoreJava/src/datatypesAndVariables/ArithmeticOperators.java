package datatypesAndVariables;

public class ArithmeticOperators {

	public static void main(String[] args) 
	{
		int a=10;
		int b=20;
		
		//+ operator
		System.out.println(a+b);
		
		//- Operator
		System.out.println(b-a);
		
		//* Operator - Multiplication
		System.out.println(a*b);
		
		// / - Division
		System.out.println(b/a);
		
		// % - Mode
		System.out.println(17%19);

	}

}
